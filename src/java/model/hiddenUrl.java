package model;

/**
 *
 * @author M7510
 */
public class hiddenUrl {
    int id;
    String url;

    public hiddenUrl() {
    }

    public hiddenUrl(int id, String url) {
        this.id = id;
        this.url = url;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    } 
}
